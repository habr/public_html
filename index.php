		<p>Тканевые натяжные потолки имеют свои особенности. Они прочные, устойчивы к износу, могут без каких-либо проблем служить более 10 лет. Такое покрытие абсолютно экологично и не служит источником вредных веществ и неприятных запахов во время эксплуатации. Для его установки не нужна тепловая пушка и владельцы помещения могут не беспокоиться о безопасности.</p>
